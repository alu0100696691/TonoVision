package noe;

public class Punto {
	
	private int x;
	private int y;

	public Punto(){
		x = -1;
		y = -1;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	

}
